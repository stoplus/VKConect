public class Contact {
    private String name;
    private  String tel;

    public Contact(String name, String tel) {
        this.name = name;
        this.tel = tel;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

}
















